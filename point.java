public class point {
		
	public int x;
	public int y;
	
	public point(int x, int y){
		 this.x=x;
		 this.y=y;
	}
	public void setX(int x){
		this.x=x;
	}
	public void setY(int y){
		this.y=y;
	}
	public String toString(){
		return "("+this.x+","+this.y+")";
	}
	public boolean equals(point p){
		if(x==p.x&&y==p.y){
			return true;
		}else{
			return false;
		}
	}
}
